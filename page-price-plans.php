function dropdown(classname) {
    var collapsible = document.querySelectorAll(".collapsible." + classname);
    var arr = Array.from(collapsible);
    arr.forEach(function (item) {
        if (item.style.display === 'table-row') {
            item.style.display = '';
            event.target.parentNode.classList.remove("active");
        }
        else {
            item.style.display = 'table-row';
            event.target.parentNode.classList.add("active");
        }
    });
}


var acc = document.getElementsByClassName("accordion");
var i;

for (i = 0; i < acc.length; i++) {
  acc[i].addEventListener("click", function() {
    this.classList.toggle("active");
    var panel = this.nextElementSibling;
    if (panel.style.display === "block") {
      panel.style.display = "none";
    } else {
      panel.style.display = "block";
    }
  });
}



function openfeature(evt, cityName) {
  var i, featuretabcontent, tablinks;
  featuretabcontent = document.getElementsByClassName("featuretabcontent");
  for (i = 0; i < featuretabcontent.length; i++) {
    featuretabcontent[i].style.display = "none";
  }
  tablinks = document.getElementsByClassName("tablinks");
  for (i = 0; i < tablinks.length; i++) {
    tablinks[i].className = tablinks[i].className.replace(" active", "");
  }
  document.getElementById(cityName).style.display = "block";
  evt.currentTarget.className += " active";
}
