    <h2>How does it work?<h2>
        <h3>Achieve Case Enlightenment by following the 5 C’s of Case Management</h3>
        <p>DiscoveryControl works by streamlining the workflow of delegating tasks and setting deadlines dictated by the request type.</p>
    <ul>
      <li>
        <h3>Capture</h3>
        <p>Through a public portal or email that enables the fast, simple, and secure creation of uniformed requests straight into DiscoveryControl Accessible anywhere, anytime. Working for you before any person is on the case.</p>
      <li>
        <h3>Comprehend </h3>
        <p>Use our expertise to customize your processes. Your team uses invaluable sector knowledge to transform requests into categorized cases in two clicks. Each categorization is agreed upon, repeatable, and assigned automatic deadlines.  The risk of human error is minimized, and compliance is maintained.</p>  
      </li>
      <li>
        <h3>Collate </h3>
        <p>Collating information from within the organization is the lifeblood of a case management system. You are not expected to know everything. The solution is to delegate simple tasks to others in your organization who can get the information that is requested and will help complete the case. These delegates could be the teams managing repositories such as MailMeter and SISCIN. This process is simple, fully audited, and saves valuable staff hours whilst implementing a delegate protocol process to help streamline future requests and identify gaps.</p> 
      <li>
        <h3>Complete </h3>
        <p>Pull in all the Delegate responses to produce a fully formed case response and select from a library of rich emails to send directly to requestors and gather feedback.  </p>
      </li>
      <li>
        <h3>Curate  </h3>
        <p>This service is living and breathing, so reporting insights are essential to pinpoint bottlenecks, detect emerging risks, manage staff workloads, and receive notifications of approaching deadlines.  You can also manage the Self-Service via the public Disclosure Log.</p>
      </li>
